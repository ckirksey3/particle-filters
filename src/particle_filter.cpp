void ParticleFilter::init(double x, double y, double theta, double std[]) {
  num_particles = 100;
  std::normal_distribution<double> x_distribution(x, std[0]);
  std::normal_distribution<double> y_distribution(y, std[1]);
  std::normal_distribution<double> theta_distribution(theta, std[2]);
  std::random_device rd;
  std::default_random_engine generator( rd() );

  for (int i =0; i<num_particles; i++) {
    Particle cur_particle;
    cur_particle.x = x_distribution(generator);
    cur_particle.y = y_distribution(generator);
    cur_particle.theta = theta_distribution(generator);
    cur_particle.weight = 1;
    cur_particle.id = i;
    particles.push_back(cur_particle);
  }
  is_initialized = true;
}

