void ParticleFilter::dataAssociation(std::vector<LandmarkObs> predicted, std::vector<LandmarkObs>& observations) {
  for (LandmarkObs observation: observations) {
    double smallest_distance = INT_MAX;
    for (LandmarkObs prediction: predicted) {
      double dist_between = dist(observation.x, observation.y, prediction.x, prediction.y);
      if (dist_between < smallest_distance) {
        smallest_distance = dist_between;
        observation.id = prediction.id;
      }
    }
  }
}

